<h1 style="color: #4651f0;">Command</h1>
<p>
    Le fait d'utiliser une Commande comme modèle de concepton est que cela permet de paramétrer des méthodes avec différents paramêtres, de retarder ou de mettre en 
    file d'attente l'exécution d'une demande et de prendre en charge les opérations annulables.
</p>