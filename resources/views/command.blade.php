<h1 style="color: #4651f0;">Command</h1>
<p style="color: #545454;">
    Le fait d'utiliser une Commande comme modèle de concepton est que cela permet de paramétrer des méthodes avec différents paramêtres, de retarder ou de mettre en 
    file d'attente l'exécution d'une demande et de prendre en charge les opérations annulables.

    A partir de cela, nous pouvons directement appeler dans une route définie avec des paramètres en particulier un controller en particulier ainsi qu'une fonction du 
    controller en question afin d'éxecuter les fonctions qu'il contient, qui peut elles mêmes appeler les fonctions définies dans les classes auxquelles le controller 
    fait appel.

    Cette méthode favorise surtout, de mon point de vue, la structuration et hierarchisation du code ainsi que la lisibilité ce celui-çi. 
</p>